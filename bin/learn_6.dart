//dynamic data types
//is used to accommodate any data even numeric and string together

void main() {
  dynamic number = 100;
  number = 'number';
  number = true;
  number = 100.5;

  /*
  if you use var as not declare like 'var variable;' 
  it is automatically read as dynamic
  */
  var dynamics;
  dynamics = 10;
  dynamics = 10.5;
  dynamics = '10.5';
  dynamics = true;
}
