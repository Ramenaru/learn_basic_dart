//arithmetics operator like +, *, /

void main() {
  var resultsPlus = 66 + 66;
  var resultsMin = 66 - 66;
  var resultsNegative = -66;
  var resultsPositive = 66;
  var resultsTimes = 66 * 66;
  var resultsFor = 66 / 66;
  var resultsForButInt = 66 ~/ 66;
  var resultsModulo = 66 % 66;

  print(resultsPlus);
  print(resultsMin);
  print(resultsNegative);
  print(resultsPositive);
  print(resultsTimes);
  print(resultsFor);
  print(resultsForButInt);
  print(resultsModulo);
}
